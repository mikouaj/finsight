package pl.surreal.finance.transaction.migration;

import java.io.IOException;
import java.io.InputStream;

import com.fasterxml.jackson.core.JsonParseException;
import com.fasterxml.jackson.databind.JsonMappingException;
import com.fasterxml.jackson.databind.ObjectMapper;

public class OldConfigReader {
	final ObjectMapper mapper;
	public OldConfigReader(ObjectMapper mapper) {
		this.mapper = mapper;
	}
	
	public OldConfig parse(InputStream stream) throws JsonParseException, JsonMappingException, IOException {
		return mapper.readValue(stream, OldConfig.class);
	}
}
